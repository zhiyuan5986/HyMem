#!/usr/bin/env python3
"""Evaluate LoCoMo QA accuracy under LongLLMLingua-style turn-level compression.

For each QA pair, this script:
1) Selects golden evidence sessions.
2) Flattens session turns into documents while preserving session time lines and speaker prefixes.
3) Applies token-level contrastive filtering (LongLLMLingua-style) on turn contents for multiple compression ratios.
4) Generates answers with an LLM and scores with an LLM judge.
5) Saves per-QA per-ratio results and draws an accuracy-vs-ratio plot.
"""

from __future__ import annotations

import argparse
import json
import math
import re
from pathlib import Path
from typing import Any

import matplotlib.pyplot as plt
from openai import OpenAI

import sys
sys.path.insert(0, str(Path(__file__).parent.parent))

from hymem.data.loader import load_locomo_dataset
from llmlingua import PromptCompressor
from hymem.prompts.templates import PromptTemplates

from tqdm import tqdm

ACCURACY_PROMPT = """
You are an answer scoring expert.
You will receive the following information: (1) a question, (2) a standard (reference answer), (3) an answer generated by a memory-based LLM. Your task is to label the generated answer as CORRECT or WRONG.
Important notes:
Be as lenient as possible when scoring: the standard answer is typically a concise short sentence, while the generated answer may be longer and more detailed. As long as the generated answer aligns with the standard answer, it should be marked as CORRECT. Do not be overly nitpicky.
For time-related questions, the standard answer is a specific date, month, or year. The generated answer may be longer or use relative time expressions (e.g., "last Tuesday" or "next month"). Please be lenient in scoring—if the generated answer refers to the same time period as the standard answer, mark it as CORRECT.
Finally, provide CORRECT or WRONG.
Do not include both CORRECT and WRONG in your response, as this will cause errors in the evaluation script.
Simply return the label in JSON format with the key "label".
Now it's time for the formal question:
Question: {question}
Gold answer: {gold_answer}
Generated answer: {generated_answer}
"""

ANSWER_PROMPT = PromptTemplates.ANSWER_DEEP


def parse_session_ids(evidence_list: list[str]) -> list[int]:
    ids = set()
    for ev in evidence_list:
        if not ev:
            continue
        for match in re.findall(r"D(\d+):", str(ev)):
            ids.add(int(match))
    return sorted(ids)


def _normalize_for_substring(text: str) -> str:
    return " ".join((text or "").lower().split())


def _is_turn_retained(turn_text: str, compressed_text: str, min_substr_len: int = 24) -> bool:
    turn_norm = _normalize_for_substring(turn_text)
    compressed_norm = _normalize_for_substring(compressed_text)
    if not turn_norm or not compressed_norm:
        return False
    if turn_norm in compressed_norm:
        return True
    if len(turn_norm) >= min_substr_len:
        window = turn_norm[:min_substr_len]
        return window in compressed_norm
    return False


def compress_turn_documents(compressor: PromptCompressor, docs: list[str], question: str, ratio: float) -> str:
    if ratio >= 0.999 or not docs:
        return "\n".join(docs)

    call_variants = [
        {"context": docs, "question": question, "rate": ratio},
        {"context": docs, "instruction": "", "question": question, "rate": ratio},
        {"context": docs, "instruction": "", "question": question, "compression_ratio": ratio},
    ]

    last_error = None
    result = None
    for kwargs in call_variants:
        try:
            result = compressor.compress_prompt(**kwargs)
            break
        except TypeError as exc:
            last_error = exc
            continue

    if result is None:
        raise RuntimeError(f"Failed to call compress_prompt with known signatures: {last_error}")

    if isinstance(result, dict):
        if "compressed_prompt" in result and isinstance(result["compressed_prompt"], str):
            return result["compressed_prompt"]
        for key in ["compressed_context", "compressed_prompt_list", "context", "compressed_prompts"]:
            if key in result and isinstance(result[key], list):
                return "\n".join(str(x) for x in result[key] if str(x).strip())

    if isinstance(result, list):
        return "\n".join(str(x) for x in result if str(x).strip())
    return "\n".join(docs)


def build_context(sample, qa, compressor, ratio: float) -> str:
    session_ids = parse_session_ids(qa.evidence)
    blocks = []
    for sid in session_ids:
        session = sample.conversation.sessions.get(sid)
        if session is None:
            continue
        lines = [f"Session starts at: {session.date_time}"]
        turn_docs = []
        for turn in session.turns:
            turn_docs.append(f"Speaker {turn.speaker} says : {turn.text}")

        compressed_text = compress_turn_documents(compressor, turn_docs, qa.question, ratio)
        compressed_lines = [line.strip() for line in compressed_text.splitlines() if line.strip()]
        lines.extend(compressed_lines)
        blocks.append("\n".join(lines))
    return "\n\n".join(blocks)


def build_answer_prompt(context: str, question: str) -> str:
    return (
        f"{ANSWER_PROMPT.strip()}\n\n"
        f"Memories:\n{context}\n\n"
        f"Question: {question}\n"
        "Return JSON only."
    )


def ask_llm(client: OpenAI, model: str, prompt: str, temperature: float = 0.0) -> str:
    resp = client.chat.completions.create(
        model=model,
        messages=[{"role": "user", "content": prompt}],
        temperature=temperature,
    )
    return (resp.choices[0].message.content or "").strip()


def judge_llm(client: OpenAI, judge_model: str, question: str, gold: str, pred: str) -> int:
    text = ask_llm(
        client,
        judge_model,
        ACCURACY_PROMPT.format(question=question, gold_answer=gold, generated_answer=pred),
        temperature=0.0,
    )
    try:
        label = json.loads(text).get("label", "WRONG")
    except Exception:
        label = "CORRECT" if re.search(r"\bCORRECT\b", text, flags=re.IGNORECASE) else "WRONG"
    return 1 if str(label).upper() == "CORRECT" else 0


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--dataset", default="scripts/data/locomo10.json")
    parser.add_argument("--output-dir", default="outputs/longllmlingua_golden_eval")
    parser.add_argument("--qa-model", default="gpt-4.1-mini")
    parser.add_argument("--judge-model", default="gpt-4.1-mini")
    parser.add_argument("--base-url", default=None)
    parser.add_argument("--api-key", required=True)
    parser.add_argument("--compressor-model-name", default="NousResearch/Llama-2-7b-hf")
    parser.add_argument("--compressor-device-map", default="cuda")
    args = parser.parse_args()

    ratios = [round(i / 10, 1) for i in range(1, 11)]
    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    client = OpenAI(api_key=args.api_key, base_url=args.base_url)
    compressor = PromptCompressor(model_name=args.compressor_model_name, device_map=args.compressor_device_map)

    dataset = load_locomo_dataset(args.dataset)
    detailed: list[dict[str, Any]] = []
    ratio_scores: dict[float, list[int]] = {r: [] for r in ratios}

    for sample_idx, sample in tqdm(enumerate(dataset), desc="Processing samples"):
        for qa_idx, qa in tqdm(enumerate(sample.qa), desc="Processing QAs"):
            if qa.category == 5:
                continue
            gold = qa.final_answer or qa.answer or ""
            if not gold:
                continue
            qa_row = {"sample_idx": sample_idx, "qa_idx": qa_idx, "question": qa.question, "gold": gold, "ratios": {}}
            for ratio in ratios:
                context = build_context(sample, qa, compressor, ratio)

                raw_answer = ask_llm(client, args.qa_model, build_answer_prompt(context, qa.question), temperature=0.0)
                try:
                    answer = json.loads(raw_answer).get("answer", raw_answer)
                except Exception:
                    answer = raw_answer
                score = judge_llm(client, args.judge_model, qa.question, gold, answer)
                ratio_scores[ratio].append(score)
                qa_row["ratios"][str(ratio)] = {"answer": answer, "llm_judge": score}
            detailed.append(qa_row)
            print(qa_row)

    summary = {str(r): (sum(v) / len(v) if v else 0.0) for r, v in ratio_scores.items()}
    with open(output_dir / "per_qa_ratio_accuracy.json", "w", encoding="utf-8") as f:
        json.dump(detailed, f, ensure_ascii=False, indent=2)
    with open(output_dir / "ratio_accuracy_summary.json", "w", encoding="utf-8") as f:
        json.dump(summary, f, ensure_ascii=False, indent=2)

    x = ratios
    y = [summary[str(r)] for r in ratios]
    plt.figure(figsize=(8, 5))
    plt.plot(x, y, marker="o")
    plt.xlabel("Compression ratio")
    plt.ylabel("QA accuracy (LLM judge)")
    plt.title("LoCoMo QA Accuracy vs LongLLMLingua Compression Ratio")
    plt.grid(True, linestyle="--", alpha=0.4)
    plt.tight_layout()
    plt.savefig(output_dir / "ratio_accuracy_curve.png", dpi=200)


if __name__ == "__main__":
    main()
